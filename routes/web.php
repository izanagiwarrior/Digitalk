// Matches /
// Function : Hit Point
$router->get('/', function () use ($router) {
    return "Dont't forget to add '/api' after base url :D";
});

// API route group
$router->group(['prefix' => 'api'], function () use ($router) {

    // ==============================================================
    //  ========================== API ==============================
    //  =============================================================

    // Matches /api
    // Function : API Main Point
    $router->get('/', function () use ($router) {
        return $router->app->version();
    });
});

    // ==============================================================
    //  ======================== END API ============================
    //  =============================================================

